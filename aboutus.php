&nbsp;The first Friday in June is "National Donut Day" in the USA. The holiday was started by the Chicago Salvation Army in 1938 to celebrate Salvation Army lassies who volunteered their cooking skills in Europe. One of the foods that they made for the troops was donuts. 
&nbsp;Helen Purviance and Margaret Sheldon served the first Salvation Army donuts in 1917 when they were sent to France and joined the American First Division in the capacity of cooks. Their provisions consisted of eggs, flour, sugar, baking soda which was not enough to make a regular meal so Helen and Margaret decided to make donuts as a treat for the troops and they were called "Doughnut Girls" by the soldiers. The image below is not of a Doughnut Girl but it does show a girl eating a doughnut.
&nbsp;The Red Cross' version of the "Doughnut Girls" were called the "Doughnut Dollies" and they served donuts to the soldiers and gave them a taste of home during World War II. 

